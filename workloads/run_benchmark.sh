#!/bin/bash
# ScyllaDB per-loader benchmark runner (runs ON A SINGLE LOADER).
#
# The local orchestrator (project-root run_benchmark.sh) invokes this on every
# loader in parallel and passes a ROLE so each loader does the right thing:
#
#   --role load    : steady-state 50/50 mixed read/write using a PERSISTENT
#                    connection pool. After the initial ramp this produces
#                    ZERO new connections/s (the pool is reused). This is the
#                    "traffic" role and does NOT run the connection storm.
#   --role flood   : sits idle for FLOOD_DELAY, then runs the connection storm
#                    (`scylla perf-cql-raw --workload connect`, one process per
#                    Scylla node) for FLOOD_DURATION. No steady traffic. This is
#                    the "storm" role.
#   --role both    : runs steady load AND, after FLOOD_DELAY, overlaps the
#                    connection storm on top of it for FLOOD_DURATION. Kept for
#                    flexibility; the orchestrator no longer assigns it so that
#                    traffic and storm loaders stay cleanly separated.
#
# Phase banners are printed for BOTH the load and the storm so the log clearly
# shows what is happening at each moment.
#
# Usage:
#   run_benchmark.sh --role <load|flood|both> \
#                    [--duration 5m] [--flood-delay 120s] [--flood-duration 180s] \
#                    [--storm-connections-per-shard 416] [--storm-concurrency-per-shard 10] \
#                    [--storm-smp 0] \
#                    [--threads N] [--concurrency N] [--rate OPS_PER_SEC] \
#                    [--connections N] [--user cassandra] [--password cassandra] \
#                    <scylla-ip-1> [scylla-ip-2 ...]
#
# --rate is this loader's steady throughput in ops/s (latte -r). --concurrency is
# only the in-flight cap. latte runs quietly: -q hides the progress bar and a
# large -s/--sampling period suppresses the per-second statistics rows, so only
# phase banners, storm logs, and the final report are printed.
set -u

ROLE="both"
DURATION="5m"           # steady-state length (2m warm-up + 3m overlapping the storm)
FLOOD_DELAY="120s"      # 2m warm-up baseline before the storm fires
FLOOD_DURATION="180s"   # how long the storm lasts (3m)
# --- Connection-storm parameters (ScyllaDB `perf-cql-raw --workload connect`) ---
# The storm is generated by ScyllaDB's own native raw-CQL benchmark, not a custom
# binary. Its `connect` workload repeatedly opens -> handshakes (STARTUP+AUTH) ->
# CLOSES real CQL connections with deterministic teardown, at high per-shard
# concurrency. That concurrency is exactly what fills the server's per-shard
# uninitialized-connections semaphore (default 8) and triggers
# scylla_transport_connections_shed. In-flight connect cycles per loader-process
# = STORM_CONNECTIONS_PER_SHARD x STORM_CONCURRENCY_PER_SHARD x STORM_SMP.
# Configured at 128 connections * 1 concurrency per shard across 4 loader cores =>
# ~512 active ports/sockets at once per loader.
STORM_CONNECTIONS_PER_SHARD="128"  # perf-cql-raw --connections-per-shard
STORM_CONCURRENCY_PER_SHARD="1"   # perf-cql-raw --concurrency-per-shard
STORM_SMP="0"                      # perf-cql-raw --smp (0 = all loader cores)
THREADS="8"             # steady-load latte -t
CONCURRENCY="64"        # steady-load latte -p (in-flight request CAP per thread).
                        # Does NOT throttle throughput; use --rate for that.
CONNECTIONS=""          # steady-load latte -c (connections per shard); blank = latte default
DO_SCHEMA="0"           # whether this loader (re)creates schema + loads data
SCYLLA_USER="${SCYLLA_USER:-cassandra}"       # CQL auth user (PasswordAuthenticator)
SCYLLA_PASSWORD="${SCYLLA_PASSWORD:-cassandra}" # CQL auth password

# latte -r/--rate is the precise throughput throttle (cycles/s), set from --rate
# below. The orchestrator derives the per-loader value from the global STEADY_RATE
# (STEADY_RATE / STEADY_LOADERS) and passes it as --rate. Empty = unthrottled
# (latte runs as fast as possible). NOTE: -p/--concurrency alone does NOT limit
# throughput for cheap cache-hit reads.
RATE_ARG=""

while [[ "$#" -gt 0 ]]; do
    case "$1" in
        --role)           ROLE="$2"; shift 2 ;;
        --duration)       DURATION="$2"; shift 2 ;;
        --flood-delay)    FLOOD_DELAY="$2"; shift 2 ;;
        --flood-duration) FLOOD_DURATION="$2"; shift 2 ;;
        --storm-connections-per-shard) STORM_CONNECTIONS_PER_SHARD="$2"; shift 2 ;;
        --storm-concurrency-per-shard) STORM_CONCURRENCY_PER_SHARD="$2"; shift 2 ;;
        --storm-smp)      STORM_SMP="$2"; shift 2 ;;
        --threads)        THREADS="$2"; shift 2 ;;
        --concurrency)    CONCURRENCY="$2"; shift 2 ;;
        --rate)           [ "$2" -gt 0 ] 2>/dev/null && RATE_ARG="-r $2"; shift 2 ;;
        --connections)    CONNECTIONS="$2"; shift 2 ;;
        --user)           SCYLLA_USER="$2"; shift 2 ;;
        --password)       SCYLLA_PASSWORD="$2"; shift 2 ;;
        --schema)         DO_SCHEMA="1"; shift ;;
        --) shift; break ;;
        -*) echo "Unknown option: $1" >&2; exit 1 ;;
        *) break ;;
    esac
done

SCYLLA_IPS="$@"
if [ -z "$SCYLLA_IPS" ]; then
    echo "ERROR: no Scylla IPs provided." >&2
    exit 1
fi

WORKLOAD_PATH="$HOME/workloads/workload.rn"
[ -f "$WORKLOAD_PATH" ] || WORKLOAD_PATH="./workload.rn"
# Connection-storm generator: ScyllaDB's own `scylla perf-cql-raw` (installed on
# the loader from the same apt repo as the DB nodes). No custom binary is shipped.
STORM_BIN="$(command -v scylla || echo /usr/bin/scylla)"

HOST="$(hostname)"
banner() { echo "[$(date '+%H:%M:%S')] [$HOST] $*"; }

# All latte output (CONFIG banner + the final statistics report) is verbose and,
# when streamed back over SSH and multiplied across loaders, floods the console.
# run_latte() sends latte's stdout+stderr to a per-loader log file instead, so the
# console only shows this script's own phase banners. The full latte report stays
# on the loader at $LATTE_LOG for later inspection. On failure we surface a short
# error plus the tail of the log so problems are not silently swallowed.
LATTE_LOG="$HOME/latte-${ROLE}.log"
run_latte() {
    if "$@" >>"$LATTE_LOG" 2>&1; then
        return 0
    fi
    local rc=$?
    banner "[error] latte exited $rc — last lines of $LATTE_LOG:"
    tail -n 15 "$LATTE_LOG" 2>/dev/null | sed 's/^/    /'
    return $rc
}
: >"$LATTE_LOG" 2>/dev/null || true   # truncate any log from a previous run

CONN_ARG=""
[ -n "$CONNECTIONS" ] && CONN_ARG="-c $CONNECTIONS"

# Silence latte's noisy per-line output during the run:
#   -q/--quiet         removes the animated progress bar.
#   -s/--sampling BIG  collapses the periodic statistics log (default 1s, which
#                      prints a numbers row every second and floods the console)
#                      into a single end-of-run sample. 100000s (~27h) is longer
#                      than any run, so no intermediate rows are emitted.
# The final summary report is still printed in full.
QUIET_ARG="-q -s 100000s"

# The cluster runs PasswordAuthenticator, so EVERY latte invocation (schema,
# load, and the steady run) must authenticate or it fails to connect and no
# workload traffic is generated. latte reads --user/--password (long forms; -p
# is already --concurrency).
AUTH_ARG="--user $SCYLLA_USER --password $SCYLLA_PASSWORD"

# Optional: one designated loader prepares schema.
# This is a PREREQUISITE for every other loader: without the keyspace and the
# `latte.bench` table, the steady 50/50 workload has nothing to read/write. So
# if the schema initialization fails, abort here (non-zero).
if [ "$DO_SCHEMA" = "1" ]; then
    banner "[schema] Initializing schema..."
    if ! run_latte latte schema $AUTH_ARG "$WORKLOAD_PATH" $SCYLLA_IPS; then
        banner "[schema] FATAL: schema creation failed — aborting (see $LATTE_LOG)."
        exit 1
    fi
fi

# --- Launch the connection storm in the background (flood / both roles) -------
# The storm uses ScyllaDB's native `scylla perf-cql-raw --workload connect`, one
# process PER target Scylla node so a single loader fans its storm across the
# whole cluster. Each process opens/handshakes/closes real CQL connections at
# STORM_CONNECTIONS_PER_SHARD x STORM_CONCURRENCY_PER_SHARD x STORM_SMP in-flight
# concurrency — saturating the server's per-shard uninitialized-connections
# semaphore (default 8) to trigger scylla_transport_connections_shed.
#
# perf-cql-raw's connect workload first polls <node>:10000 (REST) until it
# returns "[]" (no compactions). That endpoint is unreachable on a real node
# (bound to 127.0.0.1 + firewalled) AND returns non-empty once steady load runs,
# so we intercept it locally: a fake HTTP server on 127.0.0.1:10000 (provisioned
# as fake-compaction-endpoint.service) always answers "[]", and we REDIRECT each
# <node>:10000 to it. Only :10000 is redirected; storm CQL (:9042) goes direct.
FLOOD_PIDS=()
STORM_LOG_DIR="$HOME"
storm_iptables_add() {
    local ip
    for ip in $SCYLLA_IPS; do
        sudo iptables -t nat -C OUTPUT -p tcp -d "$ip" --dport 10000 -j REDIRECT --to-ports 10000 2>/dev/null \
            || sudo iptables -t nat -A OUTPUT -p tcp -d "$ip" --dport 10000 -j REDIRECT --to-ports 10000 2>/dev/null || true
    done
}
storm_iptables_del() {
    local ip
    for ip in $SCYLLA_IPS; do
        sudo iptables -t nat -D OUTPUT -p tcp -d "$ip" --dport 10000 -j REDIRECT --to-ports 10000 2>/dev/null || true
    done
}
storm_cleanup() {
    for p in "${FLOOD_PIDS[@]:-}"; do kill -TERM "$p" 2>/dev/null || true; done
    storm_iptables_del
}

if [ "$ROLE" = "flood" ] || [ "$ROLE" = "both" ]; then
    trap 'storm_cleanup' EXIT INT TERM
    (
        banner "[storm] Connection storm ARMED — waiting ${FLOOD_DELAY} for baseline..."
        sleep "${FLOOD_DELAY%s}" 2>/dev/null || sleep 120
        # Ensure the fake compaction endpoint is up and the REST poll is redirected.
        sudo systemctl start fake-compaction-endpoint.service 2>/dev/null || true
        storm_iptables_add
        _dur_s="$(printf '%s' "$FLOOD_DURATION" | sed 's/[^0-9].*$//')"; [ -n "$_dur_s" ] || _dur_s=180
        _smp_arg=""; [ "$STORM_SMP" -gt 0 ] 2>/dev/null && _smp_arg="--smp $STORM_SMP"
        # HARD DEADLINE for each storm process. perf-cql-raw does not always exit
        # cleanly when its --duration elapses (e.g. it can wedge in its pre-run
        # readiness/compaction polls, or take a long time to tear down under a
        # storm), which would make the wait below block forever and hang the whole
        # benchmark. So run each one under `timeout`: give it --duration plus a
        # SHORT grace to finish on its own, then SIGTERM, then SIGKILL 3s later if
        # it still refuses to die. This guarantees the storm always terminates
        # promptly after its duration.
        _grace_s=3
        _deadline_s=$(( _dur_s + _grace_s ))
        banner "[storm] >>> CONNECTION STORM TRIGGERED for ${FLOOD_DURATION} (perf-cql-raw connect; per-node conns/shard=${STORM_CONNECTIONS_PER_SHARD} conc/shard=${STORM_CONCURRENCY_PER_SHARD}; hard deadline ${_deadline_s}s) <<<"
        pids=()
        for ip in $SCYLLA_IPS; do
            log="$STORM_LOG_DIR/storm-${ip}.log"
            : >"$log" 2>/dev/null || true
            # --overprovisioned keeps the tool friendly as a CLIENT sharing the
            # loader with others (no dedicated cores / real-time priority). NOTE:
            # do NOT pass --developer-mode here: it is a scylla-SERVER config
            # option and is rejected by perf-cql-raw's standalone (--remote-host)
            # mode, which aborts the storm.
            # --continue-after-error true is ESSENTIAL for a storm: perf-cql-raw
            # otherwise stops on the first failed connect (stop_on_error), but a
            # connection storm is SUPPOSED to provoke connect errors (the server
            # blocking/shedding/refusing new connections). Without this the storm
            # would abort the instant it starts working.
            # `timeout --kill-after=3s <deadline>` sends SIGTERM at the deadline
            # and SIGKILL 3s after that; -s TERM makes the first signal SIGTERM.
            timeout -s TERM --kill-after=3s "${_deadline_s}s" \
                "$STORM_BIN" perf-cql-raw \
                    --workload connect \
                    --remote-host "$ip" \
                    --duration "$_dur_s" \
                    --connections-per-shard "$STORM_CONNECTIONS_PER_SHARD" \
                    --concurrency-per-shard "$STORM_CONCURRENCY_PER_SHARD" \
                    --username "$SCYLLA_USER" --password "$SCYLLA_PASSWORD" \
                    --continue-after-error true \
                    $_smp_arg --overprovisioned \
                >>"$log" 2>&1 &
            pids+=($!)
            banner "[storm]   -> perf-cql-raw connect to $ip (log: $log)"
        done
        rc=0
        for p in "${pids[@]}"; do
            # timeout exits 124 if it had to SIGTERM, or 137 if it SIGKILLed the
            # process. Both mean "the storm ran its full duration and we forcibly
            # stopped it" — that is the NORMAL end of a storm, not a failure.
            prc=0; wait "$p" || prc=$?
            case "$prc" in
                0|124|137|143) : ;;   # clean, timed-out, killed, or term'd -> OK
                *) rc=1 ;;
            esac
        done
        if [ "$rc" -ne 0 ]; then
            banner "[storm] one or more perf-cql-raw processes exited with an unexpected error (see storm-*.log):"
            for ip in $SCYLLA_IPS; do tail -n 8 "$STORM_LOG_DIR/storm-${ip}.log" 2>/dev/null | sed "s/^/    [$ip] /"; done
        fi
        storm_iptables_del
        banner "[storm] <<< CONNECTION STORM ENDED >>>"
    ) &
    FLOOD_PIDS=($!)
fi

# --- Steady-state load (load / both roles) ------------------------------------
# Track the steady-load outcome so we can exit non-zero if latte failed. A failed
# steady run means this loader produced NO benchmark traffic; the orchestrator
# must see that as a failure rather than a clean run.
LOAD_RC=0
if [ "$ROLE" = "load" ] || [ "$ROLE" = "both" ]; then
    banner "[load] >>> STEADY-STATE 50/50 mixed load STARTED (persistent pool, 0 new conns) <<<"
    run_latte latte run $QUIET_ARG $AUTH_ARG -f read:0.5 -f write:0.5 \
        -d "$DURATION" \
        -t "$THREADS" \
        -p "$CONCURRENCY" \
        $RATE_ARG \
        $CONN_ARG \
        "$WORKLOAD_PATH" $SCYLLA_IPS || LOAD_RC=$?
    if [ "$LOAD_RC" -ne 0 ]; then
        banner "[load] ERROR: steady-state load exited $LOAD_RC — no traffic generated (see $LATTE_LOG)."
    else
        banner "[load] <<< STEADY-STATE load FINISHED >>>"
    fi
elif [ "${#FLOOD_PIDS[@]}" -gt 0 ]; then
    # flood-only role: just wait for the flood to complete.
    banner "[storm] flood-only role; waiting for flood to finish..."
fi

# --- Reap the flood -----------------------------------------------------------
if [ "${#FLOOD_PIDS[@]}" -gt 0 ]; then
    wait "${FLOOD_PIDS[0]}" 2>/dev/null || true
    banner "[storm] flood process reaped."
fi

banner "Run complete (role=$ROLE)."
# Propagate a failed steady-state load as this loader's exit status so the
# orchestrator flags the run instead of reporting a clean success.
exit "$LOAD_RC"
